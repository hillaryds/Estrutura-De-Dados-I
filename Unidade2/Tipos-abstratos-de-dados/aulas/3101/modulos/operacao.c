#include "operacao.h"


float soma_float( float a, float b){
    float c = a + b;
    return c;
}


float sub_float(float b, float a){
    float c;
    c = b - a;
    return c;
}


float mult_float(float a, float b){
    float c;
    c = b * a;
    return c;
}

float div_float(float a, float b){
    float c = a / b;
    return c;
}